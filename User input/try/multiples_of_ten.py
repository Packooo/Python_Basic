number = int(input("Enter a number: "))
if number % 10 == 0:
    print("The number is a multiple of 10.")
else:
    print("The number is not a multiple of 10.")
    
# Explanation:
# The code takes an integer input from the user and checks if it is a multiple of 10.
# If the remainder when the number is divided by 10 is 0, it prints that the number is a multiple of 10.
# Otherwise, it prints that the number is not a multiple of 10.