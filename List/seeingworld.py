lokasi = ['yogyakarta', 'manado', 'tomohon', 'miyagi']
print(lokasi)
print("menggunakan sorted")
print(sorted(lokasi))
print("print lokasi asli")
print(lokasi)

print("menampilkan sorted reverse alpabet tanpa merubah list")
print(sorted(lokasi, reverse=True))
print(lokasi)

print("menggunakan sort")
lokasi.sort()
print(lokasi)

print("menggunakan sort reverse")
lokasi.sort(reverse=True)
print(lokasi)

